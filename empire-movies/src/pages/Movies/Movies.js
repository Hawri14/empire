import React, { useState } from 'react'
import axios from 'axios'
import Search from '../Movies/MovieSearch'
import Results from '../Movies/MovieResults'
import Popup from '../Movies/MoviePop'
import Result from '../Movies/MovieResult'
import '../Movies/MoviePage.css'

function App() {
  const [state, setState] = useState({ //create our states and give it a value
    s: "", //search query
    results: [], //empty array
    selected: {}
  });
  const apiurl = "http://www.omdbapi.com/?apikey=bfa7062e";

  const search = (e) => {
    if (e.key === "Enter") {
      axios(apiurl + "&s=" + state.s).then(({ data }) => {
        let results = data.Search;

        setState(prevState => {
          return { ...prevState, results: results }
        })
      });
    }
  }
  
  const handleInput = (e) => {
    let s = e.target.value;

    setState(prevState => {
      return { ...prevState, s: s }
    });
  }

  const openPopup = id => {
    axios(apiurl + "&i=" + id).then(({ data }) => {
      let result = data;

      console.log(result);

      setState(prevState => {
        return { ...prevState, selected: result }
      });
    });
  }

  const closePopup = () => {
    setState(prevState => {
      return { ...prevState, selected: {} }
    });
  }

  return (
    <div className="App">
      <header>
        <h1>Empire Movies Selection</h1>
      </header>
      <main>
        <Search handleInput={handleInput} search={search} />

        <Results results={state.results} openPopup={openPopup} />

        {(typeof state.selected.Title != "undefined") ? <Popup selected={state.selected} closePopup={closePopup} /> : false}
      </main>
    </div>
  );
}

export default App